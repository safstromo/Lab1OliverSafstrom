package se.iths;


// Object per hour.
public class Hour {

    private String name;
    private int price;

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public void setName(String newName) {
        name = newName;
    }

    public void setPrice(int newInt) {
        price = newInt;
    }
}
